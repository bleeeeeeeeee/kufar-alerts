def region_slug_from_id(region_id: int | str | None) -> str | None:
    try:
        rid = int(region_id)
    except (TypeError, ValueError):
        return None
    for slug, value in REGION_SLUGS.items():
        if value == rid:
            return slug
    return None


def region_name(region_id: int | str | None) -> str:
    if region_id is None:
        return ""
    try:
        return REGIONS.get(int(region_id), str(region_id))
    except (TypeError, ValueError):
        return str(region_id)


def area_name(region_id: int | str | None, area_id: int | str | None) -> str:
    if region_id is None or area_id is None:
        return ""
    try:
        return AREAS.get(int(region_id), {}).get(int(area_id), str(area_id))
    except (TypeError, ValueError):
        return str(area_id)


def format_location(params: dict) -> str:
    rgn = params.get("rgn")
    ar = params.get("ar")
    if not rgn:
        return ""
    region = region_name(rgn)
    if ar:
        city = area_name(rgn, ar)
        if region == "Минск":
            return f"Минск, {city}"
        return f"{region}, {city}"
    return region
